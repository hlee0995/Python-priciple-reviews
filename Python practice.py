from random import *
from math import *

print(random()) #0.0 ~ 1.0 미만의 값 생성
print(random() *10 ) # 0.0 ~ 10.0 미만의 값 생성
print(int(random())) #0 ~ 10 사이의 정숫값 생성

print(int(random() *45) +1) #1~45 사이의 정숫값 int(random()) 이 0처럼

print(randrange(1, 46)) #1~46 미만의 값 

print(randint(1, 45)) #1~45 사이의 값





print(abs(-5)) #절댓값
print(pow(4,2)) #파워
print(max(5, 12)) #맥스
print(min(5,12)) #미니멈
print(round(3.14))
print(round(4.99)) #라운딩


print(sqrt(16)) #스퀘어
print(floor(4.99)) #내림
print(ceil(3.14)) #높임

